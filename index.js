// Event listener for window load event

window.addEventListener("load", () => {
  const searchParams = new URLSearchParams(window.location.search);
  const foundIds = searchParams.get("found")?.split(",") || [];

  // Initialize the map using Leaflet.js
  var map = L.map("map", {
    preferCanvas: true,
    minZoom: -7,
    maxZoom: 4,
    center: [0, 0],
    zoom: -3,
    cursor: true,
    crs: L.CRS.Simple, // Specifies that the map uses simple Cartesian coordinates
  });

  map.on("resize");

  const markerLayer = new L.FeatureGroup();
  map.addLayer(markerLayer);
  var showMarkers = true;

  // Define bounds for map
  let leftBottom = map.unproject([-6000, 5000], 0);
  let topRight = map.unproject([6000, -5000], 0);
  let bounds = new L.LatLngBounds(leftBottom, topRight);
  map.setMaxBounds(bounds);

  // Define background image layers
  let skyLayerBackgroundImage = L.imageOverlay("images/sky.jpg", bounds);
  let surfaceLayerBackgroundImage = L.imageOverlay(
    "images/surface.jpg",
    bounds
  );
  let depthsLayerBackgroundImage = L.imageOverlay("images/depths.jpg", bounds);

  const showSurface = () => {
    // Show surface layer, hide other layers
    map.removeLayer(skyLayerBackgroundImage);
    surfaceLayerBackgroundImage.addTo(map);
    map.removeLayer(depthsLayerBackgroundImage);
  };

  const showSky = () => {
    // Show sky layer, hide other layers
    skyLayerBackgroundImage.addTo(map);
    map.removeLayer(surfaceLayerBackgroundImage);
    map.removeLayer(depthsLayerBackgroundImage);
  };

  // Event listener for 'show-layer-sky' button click
  $("#show-layer-sky").click(showSky);
  // Event listener for 'show-layer-surface' button click
  $("#show-layer-surface").click(showSurface).trigger("click"); // Trigger the click event immediately on page load
  $("#toggle-markers").click(() => {
    if (showMarkers) {
      map.removeLayer(markerLayer);
    } else {
      map.addLayer(markerLayer);
    }
    showMarkers = !showMarkers;
  });

  map.on("zoomend", () => {
    if (map.getZoom() < -3) {
      map.removeLayer(markerLayer);
      showMarkers = false;
    } else {
      map.addLayer(markerLayer);
      showMarkers = true;
    }
  });

  const getMarker = ({ x, y, height, id }) => {
    const coordinates = new L.LatLng(y, x);
    const marker = new L.marker(coordinates);

    marker.bindPopup(
      // Attaches a popup with the position to the marker
      `
          <div class='totk-marker'>
            <div class='totk-marker-meta'>
                <div><b>X:</b> ${x}</div>
                <div><b>Y:</b> ${y}</div>
                <div><b>Z:</b> ${height}</div>
            </div>
            <button class="btn btn-sm" id="found-btn-${id}">Check</button>
            <button class="btn btn-danger btn-sm" id="not-found-btn-${id}">Uncheck</button>
          </div>
      `
    );

    return marker;
  };

  var foundMarkers = [];

  koroks.forEach((korok, index) => {
    const marker = getMarker(korok);
    if (foundIds.includes(index.toString())) {
      marker.setOpacity(0.2);
      foundMarkers.push(index.toString());
    }
    marker.addTo(markerLayer);
  });

  map.on("popupopen", (e) => {
    const marker = e.popup._source;
    $("[id^='found-btn-']").click((e) => {
      marker.setOpacity(0.2);
      marker.closePopup();

      const currentId = e.target.id.split("-")[2];
      foundMarkers.push(currentId);
    });

    $("[id^='not-found-btn-']").click((e) => {
      marker.setOpacity(1);
      marker.closePopup();

      const currentId = e.target.id.split("-")[3];
      foundMarkers = foundMarkers.filter((id) => id !== currentId);
    });
  });

  $("#refresh-url").click(() => {
    console.log(foundMarkers);
    const baseUrl = window.location.href.split("?")[0];
    window.location.href = baseUrl + `?found=${foundMarkers.join(",")}`;
  });
});

